package com.suchteam.opendata;

public class SubscribeAssertion {

	private String subscibeAssertionId;
	private String value;
	private String assertion;
	private Subscribe subscribe;
	
	
	public String getSubscibeAssertionId() {
		return subscibeAssertionId;
	}
	public void setSubscibeAssertionId(String subscibeAssertionId) {
		this.subscibeAssertionId = subscibeAssertionId;
	}
	public String getValue() {
		return value;
	}
	public void setValue(String value) {
		this.value = value;
	}
	public String getAssertion() {
		return assertion;
	}
	public void setAssertion(String assertion) {
		this.assertion = assertion;
	}
	public Subscribe getSubscribe() {
		return subscribe;
	}
	public void setSubscribe(Subscribe subscribe) {
		this.subscribe = subscribe;
	}
	
	
	
}
